package Utilities;

/**
 *
 * @author pauva
 */
public class RespuestasOBJ {
    String pregunta;
    String respuesta;

    public RespuestasOBJ() {
    }

    public RespuestasOBJ(String pregunta, String respuesta) {
        this.pregunta = pregunta;
        this.respuesta = respuesta;
    }

    public String getPregunta() {
        return pregunta;
    }

    public void setPregunta(String pregunta) {
        this.pregunta = pregunta;
    }

    public String getRespuesta() {
        return respuesta;
    }

    public void setRespuesta(String respuesta) {
        this.respuesta = respuesta;
    }
    
    
    
}
